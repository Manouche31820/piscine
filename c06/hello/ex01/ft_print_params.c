#include <unistd.h>

int	main(int ac, char **av)
{
	int	x;
	int	y;

	(void) ac;
	x = 0;
	y = 1;
	while (av[y])
	{
		while (av[y][x])
		{
			write (1, &av[y][x], 1);
			x++;
		}
		x = 0;
		write (1, "\n", 1);
		y++;
	}
	return (0);
}
