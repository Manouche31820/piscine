int	ft_str_is_uppercase(char	*str)
{	
	int	valid;

	valid = 1;
	while (*str != '\0')
	{
		if (!(*str >= 'A' && *str <= 'Z'))
		valid = 0;
		str++;
	}
	return (valid);
}
